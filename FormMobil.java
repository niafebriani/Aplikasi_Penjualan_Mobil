    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        if (txtIdMobil.getText().equals("") || txtNamaMobil.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Masukkan Informasi Mobil");
        } else {
            try {
                mob.AksesData();
                mob.id_mobil = txtIdMobil.getText();
                mob.nama_mobil = txtNamaMobil.getText();
                mob.Edit();
                mob.res.executeUpdate(mob.sql);
                mob.conn.close();
                Bersih();
                isiTabel();
                
                JOptionPane.showMessageDialog(null, "Informasi Berhasil diUpdate");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Gagal Menyimpan");
                System.out.println(e.getMessage());
            }
        }
    }//GEN-LAST:event_btnEditActionPerformed

