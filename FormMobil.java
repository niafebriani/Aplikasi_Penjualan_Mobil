    public void SimpanData() {
        if (txtIdMobil.getText().equals("") || txtNamaMobil.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Informasi Harus Diisi Semua");
        } else {
            try {
                mob.AksesData();
                mob.id_mobil = txtIdMobil.getText();
                mob.nama_mobil = txtNamaMobil.getText();
                mob.Simpan();
                mob.res.executeUpdate(mob.sql);
                mob.conn.close();
                Bersih();
                isiTabel();
                
                JOptionPane.showMessageDialog(null, "Informasi Berhasil Disimpan");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Terjadi Kesalahan");
                System.out.println(e.getMessage());
            }
        }
    }

