    public void isiLaporan1() {
        try {
            jDari.removeAllItems();
            lp.AksesData();
            String sql = "SELECT tanggal FROM transaksi";
            Statement stat = lp.conn.createStatement();
            ResultSet res = stat.executeQuery(sql);
            jDari.addItem("Pilih Tanggal Transaksi");
            while (res.next()) {
                jDari.addItem(res.getString("tanggal"));
            }
        } catch (Exception e) {
        }
    }
    
