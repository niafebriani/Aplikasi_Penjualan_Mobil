    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if (txtUsername.getText().equals("") || txtPassword.getText().equals("") || txtNama.getText().equals("") || txtAlamat.getText().equals("") || txtNoHP.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Masukkan Semua Informasi Anda"); 
        } else {
            try {
                adm.AksesData();
                adm.nama = txtNama.getText();
                adm.username = txtUsername.getText();
                adm.password = txtPassword.getText();
                adm.alamat = txtAlamat.getText();
                adm.no_hp = txtNoHP.getText();
                adm.Edit();
                adm.res.executeUpdate(adm.sql);
                adm.conn.close();
                Bersih();
                isiTabel();
                
                JOptionPane.showMessageDialog(null, "Informasi Berhasil diUpdate");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Gagal Menyimpan");
                System.out.println(e.getMessage());
            }
            
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        try {
            adm.AksesData();
            adm.username = txtUsername.getText();
            adm.Hapus();
            adm.res.executeUpdate(adm.sql);
            adm.conn.close();
            isiTabel();
            Bersih();
            
            JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Terjadi Kesalahan");
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_btnHapusActionPerformed

