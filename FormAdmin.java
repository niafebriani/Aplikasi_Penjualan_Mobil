    public void isiTabel() {
        DefaultTableModel tm = new DefaultTableModel();
        
        tm.addColumn("ID Admin");
        tm.addColumn("Nama");
        tm.addColumn("Username");
        tm.addColumn("Password");
        tm.addColumn("Alamat");
        tm.addColumn("No Telepon");
        
        try {
            adm .AksesData();
            adm.TampilData();
            Statement stat = adm.conn.createStatement();
            ResultSet res = stat.executeQuery(adm.sql);
            
            while(res.next()) {
                tm.addRow(new Object[] {res.getString(1), res.getString(2), res.getString(3), res.getString(4), res.getString(5), res.getString(6)});
            }tbAdmin.setModel(tm);
        } catch (Exception e) {
        }
    }
    
