    public void isiTabel() {
        DefaultTableModel tm = new DefaultTableModel();
        
        tm.addColumn("ID Mobil");
        tm.addColumn("Nama Mobil");
        
        try {
            mob.AksesData();
            mob.TampilData();
            Statement stat = mob.conn.createStatement();
            ResultSet res = stat.executeQuery(mob.sql);
            while(res.next()) {
                tm.addRow(new Object[] {res.getString(1), res.getString(2)});
            }tbMobil.setModel(tm);
        } catch (Exception e) {
        }
    }
    
