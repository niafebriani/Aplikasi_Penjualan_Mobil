    private void btnCetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCetakActionPerformed
        // TODO add your handling code here:
        JasperReport jasRep;
        JasperPrint jasPri;
        Map<String, Object> transaksi = new HashMap<>();
        JasperDesign jasDes = null;
        try {
            lp.AksesData();
            File report1 = new File ("src/penjualanmobil/rptLaporan.jrxml");
            jasDes = JRXmlLoader.load(report1);
            transaksi.clear();
            lp.penjualan1 = jDari.getSelectedItem().toString();
            lp.penjualan2 = jSampai.getSelectedItem().toString();
            transaksi.put("newPenjualan1", lp.penjualan1);
            transaksi.put("newPenjualan2", lp.penjualan2);
            jasRep = JasperCompileManager.compileReport(jasDes);
            jasPri = JasperFillManager.fillReport(jasRep, transaksi , lp.conn);
            JasperViewer.viewReport(jasPri, false);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnCetakActionPerformed

